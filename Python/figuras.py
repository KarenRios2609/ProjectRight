nl = input("Ingresa el numero de lados de una figura geometrica")
nl = int(nl)

if(nl == 3):
    print("Es un triangulo")
else:
    if(nl == 4):
        print("Es un cuadrado")
    else:
        if(nl == 5):
            print("Es un pentagono")
        else:
            if(nl == 6):
                print("Es un hexagono")
            else:
                if(nl == 8):
                    print("Es un octagono")
                else:
                    if(nl == 10):
                        print("Es un decagono")
                    else:
                        if(nl == 12):
                            print("Es un dodecagono")
                    
            
